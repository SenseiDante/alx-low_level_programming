/**
 * _memset - fills the first n bytes
 * @n: bytes of the memory area
 * @s: memory pointer
 * @b: desired value
 *
 * Return: changed array with new value
 */

char *_memset(char *s, char b, unsigned int n)
{
	int i = 0;

	for (; n > 0; i++)
	{
		s[i] = b;
		n--;
	}
	return (s);
}
